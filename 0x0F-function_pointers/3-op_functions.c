#include "3-calc.h"
/**
* op_add - returns sum of two ints
* @a: first int
* @b: second int
* Return: Sum ==> a+b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - returns the diff of two ints
* @a: first int
* @b: second int
* Return: Difference ==> a-b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul -rReturns product of two ints
* @a: first int
* @b: second int
* Return: Product ==> a*b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - returns quotient of two ints
* @a: first int
* @b: second int
* Return: Quotient ==> a/b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - returns the modulus of two ints
* @a: first int
* @b: second int
* Return: Modulo ==> a%b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
